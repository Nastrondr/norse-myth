// 工具函数
export function getCreatureById(id) {
  return norseCreatures.find(creature => creature.id === id)
}

export function getCreaturesByCategory(category) {
  if (!category) return norseCreatures
  return norseCreatures.filter(creature => creature.category === category)
}

